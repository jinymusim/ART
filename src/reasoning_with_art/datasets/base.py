from abc import ABC, abstractmethod
from typing import Any

DATASET_REGISTRY: dict[str, type["BenchmarkDataset"]] = {}


def register_dataset(name: str):
    def decorator(cls):
        DATASET_REGISTRY[name] = cls
        return cls

    return decorator


class BenchmarkDataset(ABC):
    @property
    @abstractmethod
    def name(self) -> str: ...

    @abstractmethod
    def load(self, num_samples: int | None = None, split: str = "test") -> list[dict]:
        """Load dataset and return list of {"question": str, "ground_truth": str, "metadata": dict}.

        Args:
            num_samples: Maximum number of examples to return (None = all).
            split: Which split to load — "train" or "test".
        """
        ...

    @abstractmethod
    def score(self, example: dict, model_output: str) -> float:
        """Score a model output against the example. Returns float in [0, 1].

        Args:
            example: The full example dict (with question, ground_truth, metadata).
            model_output: The full text generated by the model.
        """
        ...

    def train_reward(self, example: dict, model_output: str) -> float:
        """Reward used during GRPO training (float in [0, 1]).

        Default: identical to the eval ``score`` (strictly binary task score, no
        partial/shaping credit). Most benchmarks override this with a shaped
        reward that keeps a fully-correct answer at 1.0 but credits format
        compliance / numeric proximity, so GRPO groups get gradient signal even
        before any completion is fully correct — see
        ``datasets/evaluation/reward_shaping.py`` (gsm8k, svamp, the
        multiple-choice base) and ``ToolMindDataset.train_reward``.
        """
        return self.score(example, model_output)

    def as_mlflow_dataset(self, examples: list[dict], split: str) -> Any:
        """Wrap the loaded examples in an mlflow.data.Dataset for log_input().

        Generic across loaders: serialises just (question, ground_truth) — the
        only fields shared by every benchmark. Subclasses can override to
        include benchmark-specific metadata columns.
        """
        import mlflow.data
        import pandas as pd

        df = pd.DataFrame([{"question": ex["question"], "ground_truth": ex["ground_truth"]} for ex in examples])
        return mlflow.data.from_pandas(
            df,
            source=self.name,
            name=f"{self.name}/{split}",
        )
